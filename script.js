// --- LÓGICA DE NAVEGACIÓN Y LOGIN ---

function checkLogin() {
    const pass = document.getElementById('password').value;
    
    if(pass === "18") {
        if(!localStorage.getItem('vhaez_game_complete')) {
            showScreen('game-screen');
        } else {
            showScreen('main-screen');
        }
    } else {
        alert("Acceso denegado. Revisa la edad.");
        document.getElementById('password').value = "";
    }
}

function showScreen(id) {
    document.querySelectorAll('.screen').forEach(s => {
        s.classList.remove('active');
        s.style.display = 'none';
    });
    
    const target = document.getElementById(id);
    if(target) {
        target.classList.add('active');
        target.style.display = 'flex';
    }
}

// --- LÓGICA DEL MINIJUEGO ---

function moveButton() {
    const btn = document.getElementById('btn-si');
    const padding = 60; 
    
    const maxX = window.innerWidth - btn.offsetWidth - padding;
    const maxY = window.innerHeight - btn.offsetHeight - padding;
    
    const randomX = Math.max(padding, Math.floor(Math.random() * maxX));
    const randomY = Math.max(padding, Math.floor(Math.random() * maxY));
    
    btn.style.position = 'fixed';
    btn.style.left = randomX + 'px';
    btn.style.top = randomY + 'px';
    btn.style.zIndex = "1000";
}

function finishGame() {
    localStorage.setItem('vhaez_game_complete', 'true');
    showScreen('main-screen');
}

